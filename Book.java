package library.PO;

public class Book {
	public String bookID;
	public String bookName;
	public String authorName;
	public float price;
	public String isbn;
	public String info;
	public Integer stocknum;
	public String findID;
	public String booktypeID;
	public String clcID1;
	public Integer clcID2;
	public String getBookID() {
		return bookID;
	}
	public void setBookID(String bookID) {
		this.bookID = bookID;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public Integer getStocknum() {
		return stocknum;
	}
	public void setStocknum(Integer stocknum) {
		this.stocknum = stocknum;
	}
	public String getFindID() {
		return findID;
	}
	public void setFindID(String findID) {
		this.findID = findID;
	}
	public String getBooktypeID() {
		return booktypeID;
	}
	public void setBooktypeID(String booktypeID) {
		this.booktypeID = booktypeID;
	}
	public String getClcID1() {
		return clcID1;
	}
	public void setClcID1(String clcID1) {
		this.clcID1 = clcID1;
	}
	public Integer getClcID2() {
		return clcID2;
	}
	public void setClcID2(Integer clcID2) {
		this.clcID2 = clcID2;
	}
	
	
	
}
